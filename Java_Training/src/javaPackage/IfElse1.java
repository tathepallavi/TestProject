package javaPackage;

public class IfElse1 {

	public static void main(String[] args) {
		
		int a = 15;  // local variable
		if(a > 10) {
			System.out.println("a is greater than 10");  // True condition will execute
		}
		
		
		int b = 10;
		 if(b > 15) {
			 System.out.println("b is greater than 15");  // false condition will not execute
		 }

	}

}
