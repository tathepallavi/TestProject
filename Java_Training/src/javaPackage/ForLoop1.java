package javaPackage;

public class ForLoop1 {

	public static void main(String[] args) {

		for(int i = 0; i < 5; i++) {
			
			System.out.println("value of i is " + i);
		}
		
		
		// will print this line only after coming out from loop
		System.out.println("control will come out of loop"); 
	}

}
