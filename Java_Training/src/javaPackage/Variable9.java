package javaPackage;

public class Variable9 {

	public static void main(String[] args) {
		
		int b ; // declaration of local variable
		b = 10 ; // assigning value to b variable
		System.out.println(b);  // 10 - calling b variable
		
		char c = 'h'; // defining a variable
		System.out.println(c); // h - calling c variable
		
		String d = "a";
		System.out.println(d); // a
		
		String str = "Hello Selenium";
		System.out.println(str); // Hello Selenium

	}

}
