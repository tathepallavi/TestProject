package javaPackage;

public class Variable5 {
	
	public static void main(String[] args) {
		
		int x = 10;  //Defining local variable
		
		//calling x variable for printing value
		System.out.println(x); // 10
		
		//calling x variable for initialising y
		int y = x; 
		System.out.println(y); //10
		

	}

}
